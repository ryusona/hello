public class CelsiusToFahrenheit {
    public static void main(String args[]){

        double Celsius = 19;
        double CelsiusToFahrenheit = (Celsius * 1.8) + 32;

        System.out.println("기존 섭씨 값 : " + Celsius);
        System.out.println("화씨 변경 값 : " + CelsiusToFahrenheit);

    }

}
